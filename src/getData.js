/**
 * Return a ChartXpress data object for the linkage group plot.
 */
export default function getData(linkageGroup, markers, qtls) {
    if (linkageGroup && markers && qtls) {
        const lgTrack = {
            "type": "box",
            "data": [
                {
                    "key": linkageGroup.objectId,
                    "fill": "purple",
                    "id": linkageGroup.identifier,
                    "data": [
                        [
                            0,
                            linkageGroup.length
                        ]
                    ],
                    "outline": "black"
                }
            ]
        };
        
        const markerData = [];
        markers.forEach(marker => {
            markerData.push({
                "fill": "darkred",
                "id": marker.markerName,
                "offset": marker.position,
                "outline": "black"
            });
        });
        const markerTrack = {
            "type": "triangle",
            "data": markerData
        };
        
        const qtlData = [];
        qtls.forEach(qtl => {
            qtlData.push({
                "key": qtl.objectId,
                "fill": "yellow",
                "id": qtl.identifier,
                "data": [
                    [
                        qtl.start,
                        qtl.end
                    ]
                ],
                "outline": "black"
            });
        });
        const qtlTrack = {
            "type": "box",
            "data": qtlData
        };
            
        return { tracks: [ lgTrack, markerTrack, qtlTrack ] };

    } else {

        return null;
    }
}
